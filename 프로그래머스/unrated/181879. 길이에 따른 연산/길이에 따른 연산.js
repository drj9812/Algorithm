function solution(num_list) {
    if (num_list.length >= 11) {
        let sum = 0;
        for (number of num_list) {
            sum += number;
        }
        return sum;
        
    } else {
        let multiply = 1;
        for (number of num_list) {
            multiply *= number;
        }
        return multiply;
    }
}